# Valor de tipo str
name = "Alexis"
print(type(name))

# Valor de tipo int
age = 35
print(type(age))

# Valor de tipo float
random = 35.5
print(type(random))

# Valor de tipo complex
complex = 35.5 + 45j
print(type(complex))

#Valor de tipo list
food_list = ["macarrones", "patatas", "filete"]
print(type(food_list))

# Valor de tipo tuple
food_tuple = ("macarrones", "patatas", "filete")
print(type(food_tuple))

# Valor de tipo range
number_range = range(8)
print(type(number_range))

# Valor de tipo diccionario
food_dict = {1: "macarrones", 2: "patatas", 3: "manzanas"}
print(type(food_dict))

# Valor de tipo set
food_set = {"macarrones", "patatas","manzanas"}
print(type(food_set))

# Valor de tipo bolleano
bool_balue = True
print(type(bool_balue))